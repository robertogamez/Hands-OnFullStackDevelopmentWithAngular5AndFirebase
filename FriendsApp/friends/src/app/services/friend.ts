export class Friend {
    name: string;
    mobile: string;
    email: string;
    uid: string;
    time: string;
    image: string;

    constructor(
        name: string,
        mobile: string,
        email: string,
        uid: string,
        time: string,
        image: string
    ) {
        this.name = name;
        this.mobile = email;
        this.email = email;
        this.uid = uid;
        this.time = time;
        this.image = image;
    }
}
